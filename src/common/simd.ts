async function isWasmSimdSupported(): Promise<boolean> {
  if (!WebAssembly.validate) return false;

  const simdModule = new Uint8Array([
    0x00, 0x61, 0x73, 0x6D,
    0x01, 0x00, 0x00, 0x00,
    0x01, 0x08, 0x01, 0x60, 0x00, 0x00,
    0x03, 0x02, 0x01, 0x00,
    0x0A, 0x09, 0x01, 0x07, 0x00,
    0xFD, 0x00, 0xFD, 0x00, 0x0B
  ]);

  try {
    await WebAssembly.compile(simdModule);
    return true;
  } catch {
    return false;
  }
}
